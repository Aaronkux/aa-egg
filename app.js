class AppBootHook {
  constructor(app) {
    this.app = app
  }
  
  async serverDidReady() {
    const ctx = this.app.createAnonymousContext()
    await this.app.model.sync({ force: true })
    const server = await ctx.model.Server.create(
      {
        name: "sea",
        version: "6.1.9",
        activities: data.activities,
        continents: data.continents,
      },
      {
        include: [
          {
            association: ctx.model.Server.Activity,
            include: [
              {
                association: ctx.model.Activity.Event,
                include: [
                  {
                    association: ctx.model.Event.Time,
                  },
                ],
              },
            ],
          },
          {
            association: ctx.model.Server.Continent,
            include: [
              {
                association: ctx.model.Continent.Area,
                include: [{ association: ctx.model.Area.Pack }],
              },
            ],
          },
        ],
      }
    )
  }
}
module.exports = AppBootHook
